def get_currency_to_ruble_value(currency):
    if currency == 'usd' or currency == 'eur':
        user_agent = random.choice(user_agents_list)
        r = requests.get(f'https://ru.investing.com/currencies/{currency}-rub', headers={'User-Agent': user_agent}).text
        soup = BeautifulSoup(r, "lxml")
        data = soup.find("span", {"id": "last_last"}).text.replace(',', '.')
        return float(data)
    else:
        raise ValueError


